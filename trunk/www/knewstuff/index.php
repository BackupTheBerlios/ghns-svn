<h2>KNewStuff, the KDE library</h2>
<p>
Since KDE 3.3, KNewStuff is part of kdelibs and thus available to all
KDE applications. It offers a download dialog and an upload dialog,
and currently integrates with over a dozen applications.
With KNewStuffSecure, it's possible to enforce digital signatures.
</p>

<h2>Generic application: khotnewstuff</h2>
<p>
For testing repositories, the <i>khotnewstuff</i> binary is shipped with
KDE which calls the download dialog for arbitrary GHNS repositories, and
can apply filters given on the command line.
</p>

