<h2>Desktop Software</h2>
<p>
<a href="/knewstuff/">KNewStuff</a> is the C++ GHNS library for the KDE desktop.
It comes with the useful <i>khotnewstuff</i> application.<br/>
<a href="/sdlnewstuff/">SDLNewStuff</a> is a download frontend for SDL,
written in Python, and used mainly in games.<br/>
<a href="/gnome-art/">GNOME Art</a> is a download dialog for the GNOME desktop,
written in Ruby.
</p>

<h2>Server Software</h2>
<p>
<a href="/dxs/">Desktop Exchange Service</a> is a webservice which facilitates
data uploads and downloads via SOAP messages. A Perl/SOAP::Lite implementation exists.<br/>
<a href="/hotstuff/">Hotstuff</a> is a collection of scripts which manage the
catalogisation, versioning and general data processing on the server side.<br/>
<a href="/hotstuff/">Hotstuff Web Interface</a> allows users to view GHNS repository
contents, upload new data via HTTP, and administer contents and translations.<br/>
</p>

<h2>Standard Specification and Documentation</h2>
<p>
<a href="/spec/">GHNS Standard</a> is a formal reference document which lists all
required data fields, enumerations and registered values.<br/>
<a href="/docs/">Documentation</a> is further available in form of recommendations,
tutorials and graphical explanations, here as well as external.<br/>
</p>

